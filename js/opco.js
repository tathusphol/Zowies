function openn(num){
    document.getElementById(`eye${num}`).classList.toggle('hidden');
    document.getElementById(`pickcart${num}`).classList.toggle('hidden');
    document.getElementById(`pickcart${num}`).classList.toggle('animatetextcart');
    document.getElementById(`price${num}`).classList.toggle('hidden');
}
function closee(num){
    document.getElementById(`eye${num}`).classList.toggle('hidden');
    document.getElementById(`pickcart${num}`).classList.toggle('hidden');
    document.getElementById(`pickcart${num}`).classList.toggle('animatetextcart');
    document.getElementById(`price${num}`).classList.toggle('hidden');
}